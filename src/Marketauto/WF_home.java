    public String querry(String ro,String type)
    {
        String q;
        if(type==null)
        {
            q="";
        }
        else{
            q=" and farmer_product.type='"+type+"'";
        }
        if(ro.equals("wholesaler"))
        {
            return "SELECT pid, type,quantity,quality,description,desired_price FROM farmer_product where is_sold=0"+q;
        }
        else
        {
            return "SELECT wholesaler_purchase.Pid,farmer_product.Type,sum(wholesaler_purchase.Quantity) as Quantity,farmer_product.Quality,farmer_product.Description,wholesaler_purchase.Sell_Price FROM wholesaler_purchase INNER JOIN farmer_product ON farmer_product.Pid = wholesaler_purchase.Pid and wholesaler_purchase.is_sold=0"+q+" group by wholesaler_purchase.pid";
        }
    }
    

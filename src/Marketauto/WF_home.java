    public void acceptoffers(String pid,int quan, int remquan, String price,String ofid)
    {
            int sellerid=getsellerid(pid);
            updatewholesalerdata(pid,sellerid,uid,quan,price,ofid);
            updatefarmerdata(remquan,pid);
    }
    
    public void updatewholesalerdata(String pid,int sellerid,int uid,int quan, String price,String ofid)
    {
        try
        {
                    Connection con=new connection().connect();
                    ps=con.prepareStatement("insert into wholesaler_purchase (pid, Seller_id, Buyer_id, Quantity, Cost_price, Sell_price) values (?, ?, ?, ?, ?,?)");
                    ps.setInt(1,Integer.parseInt(pid));
                    ps.setInt(2,sellerid);
                    ps.setInt(3,Integer.parseInt(ofid));
                    ps.setInt(4,quan);
                    ps.setInt(5,Integer.parseInt(price));
                    ps.setInt(6,0);
                    ps.execute();
        }
        catch (Exception ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void updatefarmerdata(int remquan,String pid)
    {
        try
            {
                    String querry;
                    Connection con=new connection().connect();
                    if(remquan>0)
                    {
                        querry="update farmer_product set Quantity=? where Pid="+pid;
                        ps=con.prepareStatement(querry);
                        ps.setInt(1,remquan);
                    }
                    else
                    {
                        querry="update farmer_product set Quantity=? , is_sold=? where Pid="+pid;
                        ps=con.prepareStatement(querry);
                        ps.setInt(1,remquan);
                        ps.setInt(2,1);
                    }
                    
                    ps.executeUpdate();
            } catch (Exception ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
    
    }
   
    
    public int getsellerid(String id)
    {   
        int i=0;
        try
        {
          
          Connection con=new connection().connect();
          String sql="SELECT uid FROM `farmer_product` WHERE pid = "+id;
          ps=con.prepareStatement(sql);
          rs = ps.executeQuery();
          while(rs.next())
            {
                 i = rs.getInt("uid");
            }
        }
        catch (Exception ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
          return i;
    }
    

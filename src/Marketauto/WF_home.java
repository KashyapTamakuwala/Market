    public ResultSet dispmyproduct(String r)
    {   
        rs=null;
        String sql;
        try
            {
              Connection con=new connection().connect();
              if(r.equalsIgnoreCase("Wholesaler"))
              {    
                sql="SELECT wholesaler_purchase.Sid,wholesaler_purchase.pid,sum(wholesaler_purchase.Quantity),wholesaler_purchase.Sell_Price,farmer_product.Type,farmer_product.Quality,farmer_product.Description\n" +
                    "FROM wholesaler_purchase\n" +
                    "INNER JOIN farmer_product ON farmer_product.Pid=wholesaler_purchase.Pid and wholesaler_purchase.is_sold=0\n" +
                    "GROUP by wholesaler_purchase.Pid";
              }
              else{
                  sql="Select retailer_purchase.Sid,retailer_purchase.Pid,SUM(retailer_purchase.Qunatity),retailer_purchase.Cost_Price,farmer_product.Type,farmer_product.Description,farmer_product.Quality\n" +
                      "FROM retailer_purchase\n" +
                      "INNER JOIN farmer_product on farmer_product.Pid =retailer_purchase.Pid\n" +
                      "GROUP by retailer_purchase.Pid";  
               }
              
               ps=con.prepareStatement(sql);
               rs = ps.executeQuery();
            }
          catch (Exception ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
        return rs;
    }
    
